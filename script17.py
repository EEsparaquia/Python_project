# Programming tutorial: 
# Classes

class calculator:

	def addition(x,y):
		added = x + y
		print(added)

	def substration(x,y):
		sub = x - y
		print(sub)

	def multiplication(x,y):
		mult = x * y
		print(mult)

	def division(x,y):
		div = x / y
		print(div)


# Using the class and the function:

calculator.division(10,9)